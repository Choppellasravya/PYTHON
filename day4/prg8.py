#union
a={1,2,3,4}
b={2,3,5,6}
print(a.union(b))
print(a&b)
#intersection():
a={1,2,3,4}
b={2,3,4,5}
print(a.intersection(b))
print(a&b)
#difference
a={1,2,3,4}
b={2,3,4,5}
print(a.difference(b))
print(a-b)
#disjoint
a={1,2,3,4}
b={2,3}
print(a.isdisjoint(b))
#subset
a={1,2,3,4}
b={2,3}
print(b.issubset(b))
#superset
a={1,2,3,4}
